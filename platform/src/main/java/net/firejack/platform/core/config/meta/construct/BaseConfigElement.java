package net.firejack.platform.core.config.meta.construct;

import net.firejack.platform.core.config.meta.INamedPackageDescriptorElement;
import net.firejack.platform.core.model.registry.INameProvider;
import net.firejack.platform.core.model.registry.IUIDProvider;


public abstract class BaseConfigElement implements INamedPackageDescriptorElement, INameProvider, IUIDProvider {

    protected String name;
    protected String path;
    protected String description;
    protected String uid;

    protected BaseConfigElement(String name) {
        this.name = name;
    }

    @Override
    public String getPath() {
        return path;
    }

    /**
     * @param path
     */
    public void setPath(String path) {
        this.path = path;
    }

    @Override
    public String getName() {
        return name;
    }

    @Override
    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String getDescription() {
        return description;
    }

    @Override
    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public String getUid() {
        return uid;
    }

    @Override
    public void setUid(String uid) {
        this.uid = uid;
    }
}