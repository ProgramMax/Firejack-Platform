package net.firejack.platform.api.registry.domain;

import net.firejack.platform.api.registry.model.ServerNodeType;
import net.firejack.platform.core.domain.AbstractDTO;
import org.springframework.beans.factory.config.BeanDefinition;
import org.springframework.context.annotation.Scope;
import org.springframework.stereotype.Component;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

@Component
@XmlRootElement
@Scope(BeanDefinition.SCOPE_PROTOTYPE)
@XmlAccessorType(XmlAccessType.FIELD)
public class ServerNodeConfig extends AbstractDTO {
	private static final long serialVersionUID = 2985550643111965179L;

	private String serverName;
    private String host;
    private Integer port;
    private ServerNodeType nodeType;
    private String lookup;
    private byte[] cert;

    public ServerNodeConfig() {
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public Integer getPort() {
        return port;
    }

    public void setPort(Integer port) {
        this.port = port;
    }

    public ServerNodeType getNodeType() {
        return nodeType;
    }

    public void setNodeType(ServerNodeType nodeType) {
        this.nodeType = nodeType;
    }

    public String getLookup() {
        return lookup;
    }

    public void setLookup(String lookup) {
        this.lookup = lookup;
    }

    public String getServerName() {
        return serverName;
    }

    public void setServerName(String serverName) {
        this.serverName = serverName;
    }

	public byte[] getCert() {
		return cert;
	}

	public void setCert(byte[] cert) {
		this.cert = cert;
	}
}